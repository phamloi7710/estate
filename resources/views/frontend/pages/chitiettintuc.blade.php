                                    Hòa trong không khí đón tết Trung thu rộn ràng, tối 02/10/2017, tại Hội trường công ty CP Phát triển nhà BR-VT, Công đoàn và Đoàn Thanh niên đã phối hợp tổ chức chương trình  Đêm hội trăng rằm 2017 cho Thiếu nhi là con, em cán bộ công nhân viên công ty. Với nội dung độc đáo và thú vị, chương trình đã mang đến cho các cháu thiếu nhi một Tết Trung thu tràn ngập niềm vui.

Tham dự Chương trình có ông Phan Văn Dinh – Chủ tịch Công Đoàn công ty; ông Đỗ Mạnh – Phó chủ tịch Công đoàn; ông Nguyễn Phan Nhật Tân - Phó Bí thư Đoàn khối doanh nghiệp tỉnh BR-VT; ông Nguyễn Quang Đạt – Phó Bí thư Đoàn thanh niên; ông Hoàng Chung Kiên – Phó Tổng Giám đốc công ty Bê tông và Xây lắp HODECO… cùng đông đảo các cán bộ, đoàn viên HODECO.

Tại đêm hội, các cháu được thưởng thức tiết mục múa lân, cùng chị Hằng, chú Cuội rước đèn ông sao; tham gia các tiết mục giao lưu văn nghệ, hào hứng chơi các trò chơi đố vui có thưởng…Cuối chương trình, các bạn nhỏ còn được cùng nhau Phá cỗ Trung thu và nhận nhiều món quà ý nghĩa từ Ban tổ chức. Đêm hội đã khép lại trong bầu không khí ngập tràn cảm xúc.

Đây không chỉ là sân chơi bổ ích, đem lại nhiều niềm vui, tiếng cười cho các cháu thiếu nhi, mà còn thể hiện sự quan tâm của Ban Lãnh đạo, Công đoàn, Đoàn thanh niên đối với đời sống tinh thần của mỗi gia đình và con em CBNV của công ty.

Trước đó, 01/10/2017 BCH Đoàn cũng đã đến từng gia đình CB-CNV công ty có con, em đạt thành tích xuất sắc trong năm học 2016-2017 để trao quà nhằm tạo động lực cho các cháu phấn đấu trong tương lai.

